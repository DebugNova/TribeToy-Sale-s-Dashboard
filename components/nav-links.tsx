"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

export type NavItem = { href: string; label: string; badge?: number };

// Lightweight inline icons (Lucide paths) keyed by route — no icon library, so
// nothing extra ships to the client. Anything unmapped falls back to a dot.
